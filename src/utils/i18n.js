// –ö–ª–∞—Å—Å –¥–ª—è —É–ø—Ä–∞–≤–ª–µ–Ω–∏—è –ø–µ—Ä–µ–≤–æ–¥–∞–º–∏
class I18nService {
  constructor() {
    this.currentLanguage = this.getInitialLanguage()
    this.listeners = new Set()
  }

  // –ü–æ–ª—É—á–∏—Ç—å –Ω–∞—á–∞–ª—å–Ω—ã–π —è–∑—ã–∫ –∏–∑ localStorage –∏–ª–∏ –ø–æ —É–º–æ–ª—á–∞–Ω–∏—é —Ä—É—Å—Å–∫–∏–π
  getInitialLanguage() {
    const saved = localStorage.getItem('app-language')
    if (saved && (saved === 'ru' || saved === 'en')) {
      return saved
    }
    
    // –ê–≤—Ç–æ–æ–ø—Ä–µ–¥–µ–ª–µ–Ω–∏–µ —è–∑—ã–∫–∞ –±—Ä–∞—É–∑–µ—Ä–∞
    const browserLang = navigator.language.toLowerCase()
    if (browserLang.startsWith('ru')) {
      return 'ru'
    }
    return 'en' // –ü–æ —É–º–æ–ª—á–∞–Ω–∏—é –∞–Ω–≥–ª–∏–π—Å–∫–∏–π
  }

  // –ü–æ–ª—É—á–∏—Ç—å —Ç–µ–∫—É—â–∏–π —è–∑—ã–∫
  getCurrentLanguage() {
    return this.currentLanguage
  }

  // –£—Å—Ç–∞–Ω–æ–≤–∏—Ç—å —è–∑—ã–∫
  setLanguage(language) {
    if (language !== 'ru' && language !== 'en') {
      console.warn('Unsupported language:', language)
      return false
    }

    this.currentLanguage = language
    localStorage.setItem('app-language', language)
    
    // –£–≤–µ–¥–æ–º–∏—Ç—å –≤—Å–µ—Ö —Å–ª—É—à–∞—Ç–µ–ª–µ–π –æ–± –∏–∑–º–µ–Ω–µ–Ω–∏–∏ —è–∑—ã–∫–∞
    this.listeners.forEach(listener => listener(language))
    
    console.log(`üåê –Ø–∑—ã–∫ –∏–∑–º–µ–Ω–µ–Ω –Ω–∞: ${language === 'ru' ? '–†—É—Å—Å–∫–∏–π' : 'English'}`)
    return true
  }

  // –ü–æ–¥–ø–∏—Å–∞—Ç—å—Å—è –Ω–∞ –∏–∑–º–µ–Ω–µ–Ω–∏—è —è–∑—ã–∫–∞
  subscribe(callback) {
    this.listeners.add(callback)
    return () => this.listeners.delete(callback)
  }

  // –ü–æ–ª—É—á–∏—Ç—å –ø–µ—Ä–µ–≤–æ–¥ –ø–æ –∫–ª—é—á—É
  t(path, fallback = '') {
    const keys = path.split('.')
    let value = translations[this.currentLanguage]

    for (const key of keys) {
      if (value && typeof value === 'object' && key in value) {
        value = value[key]
      } else {
        // –ü–æ–ø—Ä–æ–±—É–µ–º –≤ –¥—Ä—É–≥–æ–º —è–∑—ã–∫–µ –∫–∞–∫ fallback
        const fallbackLang = this.currentLanguage === 'ru' ? 'en' : 'ru'
        let fallbackValue = translations[fallbackLang]
        
        for (const fallbackKey of keys) {
          if (fallbackValue && typeof fallbackValue === 'object' && fallbackKey in fallbackValue) {
            fallbackValue = fallbackValue[fallbackKey]
          } else {
            fallbackValue = null
            break
          }
        }
        
        if (fallbackValue && typeof fallbackValue === 'string') {
          console.warn(`‚ö†Ô∏è Missing translation for "${path}" in ${this.currentLanguage}, using ${fallbackLang}`)
          return fallbackValue
        }
        
        console.warn(`‚ö†Ô∏è Missing translation for "${path}" in both languages`)
        return fallback || path
      }
    }

    return typeof value === 'string' ? value : (fallback || path)
  }

  // –ü–æ–ª—É—á–∏—Ç—å –≤—Å–µ —è–∑—ã–∫–∏
  getAvailableLanguages() {
    return [
      { code: 'ru', name: '–†—É—Å—Å–∫–∏–π', flag: 'üá∑üá∫' },
      { code: 'en', name: 'English', flag: 'üá∫üá∏' }
    ]
  }

  // –ü–µ—Ä–µ–∫–ª—é—á–∏—Ç—å —è–∑—ã–∫
  toggleLanguage() {
    const newLanguage = this.currentLanguage === 'ru' ? 'en' : 'ru'
    return this.setLanguage(newLanguage)
  }

  // –ü—Ä–æ–≤–µ—Ä–∏—Ç—å –ø–æ–¥–¥–µ—Ä–∂–∏–≤–∞–µ—Ç—Å—è –ª–∏ —è–∑—ã–∫
  isLanguageSupported(language) {
    return language === 'ru' || language === 'en'
  }

  // –ü–æ–ª—É—á–∏—Ç—å –Ω–∞–ø—Ä–∞–≤–ª–µ–Ω–∏–µ —Ç–µ–∫—Å—Ç–∞ –¥–ª—è —è–∑—ã–∫–∞
  getTextDirection(language = this.currentLanguage) {
    return 'ltr' // –í—Å–µ –ø–æ–¥–¥–µ—Ä–∂–∏–≤–∞–µ–º—ã–µ —è–∑—ã–∫–∏ —Å–ª–µ–≤–∞ –Ω–∞–ø—Ä–∞–≤–æ
  }

  // –ü–æ–ª—É—á–∏—Ç—å –ª–æ–∫–∞–ª—å –¥–ª—è —Ñ–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–∏—è
  getLocale(language = this.currentLanguage) {
    return language === 'ru' ? 'ru-RU' : 'en-US'
  }
}

